## Prerequisites

Before running the pipeline, please ensure that the following software packages are installed and available in your environment:

* python / python3
* plink2
* csvtk
* zstdcat
* bgzip
* datamash

## Utility Scripts

The following utility scripts are provided in the `scripts/` directory:

* wcut
* SubsetByKey.py
* KeyMapReplacer.py
* AddColumn.py
* body
* RemoveDuplicateTitle.py

For convenience, you may add the `scripts/` directory to your `PATH` (e.g., in `~/.bashrc`) so that these commands can be accessed directly from any location.
